package src.javaBasics;

public class javaDataTypes {
    public static void main(String[] args) {
        byte num1 = 126;
        short num2 = 32750;
        int num3 = 646464;
        long num4 = 848484848l;
        float num5 = 747.9393f;
        double num6 = 737.9393939;
        boolean value = true;
        char letter = 'a';

        System.out.println("The value of byte " + num1);
        System.out.println("The value of short " + num2);
        System.out.println("The value of int " + num3);
        System.out.println("The value of long " + num4);
        System.out.println("The value of float " + num5);
        System.out.println("The value of double " + num6);
        System.out.println("The value of boolean " + value);
        System.out.println("The value of char " + letter);
    }
}
