public class relationalOperator {
    public static void main(String[] args) {
        System.out.println("Relational Operator.");
        // <, >, <=, >=, ==, !=

        int num1 = 10, num2 = 15;
        System.out.println(num1 < num2);
        System.out.println(num1 <= num2);
        System.out.println(num1 > num2);
        System.out.println(num1 >= num2);
        System.out.println(num1 == num2);
        System.out.println(num1 != num2);
    }
}
