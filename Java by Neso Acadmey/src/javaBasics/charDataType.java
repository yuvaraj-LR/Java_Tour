package src.javaBasics;

public class charDataType {
    public static void main(String[] args) {
        char c1 = 'A';
        char c2 = 83;
        char c3 = '\u0041';

        System.out.println(c1);
        System.out.println(c2);
        System.out.println(c3);

        int c4 = '\uD83E';
        System.out.println(c4);
    }
}
