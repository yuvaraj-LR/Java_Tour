public class arrays {
    public static void main(String[] args) {
        int num[] = new int[5];

        num[0] = 20;
        num[1] = 30;
        num[2] = 40;
        num[3] = 50;
        num[4] = 60;

        for (int i = 0; i < 5; i++)
            System.out.println(num[i]);
    }
}